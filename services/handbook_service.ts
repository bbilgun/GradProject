export class HandbookService {
  /** Return all sections (for home screen grid) */
  static getAllSections(): HandbookSection[] {
    return SECTIONS;
  }

  /** Return a single section by slug */
  static getSectionBySlug(slug: string): HandbookSection | undefined {
    return SECTIONS.find((s) => s.id === slug);
  }

  /** Return sections by a list of IDs */
  static getSectionsByIds(ids: string[]): HandbookSection[] {
    return ids.map((id) => SECTIONS.find((s) => s.id === id)).filter(
      (s): s is HandbookSection => !!s,
    );
  }

  /** Simple keyword search across title + content (for offline fallback) */
  static searchLocal(query: string): HandbookSection[] {
    const q = query.toLowerCase();
    return SECTIONS.filter(
      (s) =>
        s.title.toLowerCase().includes(q) ||
        s.titleEn.toLowerCase().includes(q) ||
        s.description.toLowerCase().includes(q) ||
        s.content.toLowerCase().includes(q),
    );
  }

  /** Prepare text payload to send to AI for summarisation */
  static getAIContext(slug: string): string {
    const section = HandbookService.getSectionBySlug(slug);
    if (!section) return '';
    return `Дараах мэдээллийг товч тайлбарла:\n\n${section.content}`;
  }
}

export default HandbookService;
